     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $tips=new Contact();
        $tips->name=$request->name;
        $tips->contact=$request->contact;
        $tips->detail='0';
        $tips->save();
        return redirect()->route('contacts');
    }

